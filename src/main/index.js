import { app, BrowserWindow, ipcMain} from 'electron'
const fs = require('fs')

/**
 * Set `__static` path to static files in production
 * https://simulatedgreg.gitbooks.io/electron-vue/content/en/using-static-assets.html
 */
if (process.env.NODE_ENV !== 'development') {
  global.__static = require('path').join(__dirname, '/static').replace(/\\/g, '\\\\')
}

let mainWindow
const winURL = process.env.NODE_ENV === 'development'
  ? `http://localhost:9080`
  : `file://${__dirname}/index.html`

function createWindow () {
  /**
   * Initial window options
   */
  mainWindow = new BrowserWindow({
    height: 563,
    useContentSize: true,
    width: 1000
  })

  mainWindow.loadURL(winURL)
  mainWindow.maximize()

  mainWindow.on('closed', () => {
    mainWindow = null
  })

  let configPath = app.getPath('home')
  let configSrc = fs.readFileSync(configPath + '/smart_parking_config.json')
  let config = configSrc.toString('utf-8')

  ipcMain.on('load', event => {
    console.log(config)
    event.sender.send('asychronous-request', JSON.stringify(config))
  })
  ipcMain.on('openconsole', () => {
    mainWindow.webContents.openDevTools()
  })

 
}

app.on('ready', createWindow)

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit()
  }
})

app.on('activate', () => {
  if (mainWindow === null) {
    createWindow()
  }
})

